package com.example.firstui;

public class ClassNama {
    private String Nama;

    //inisialisasi objek
    public ClassNama(String nama)
    {
        this.Nama = nama;
    }

    //membuat method getnam euntuk mengembalikan nilai ke variabel nama
    public String getName()
    {
        return this.Nama;
    }
}
